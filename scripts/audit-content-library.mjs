import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const repoRoot = path.resolve(import.meta.dirname, "..");
const processedRoot = path.join(repoRoot, "content", "processed");
const checklistRoot = path.join(repoRoot, "content", "checklists");
const reportPath = path.join(checklistRoot, "coverage-report.md");
const healthPath = path.join(repoRoot, "public", "data", "content-health.json");

const REQUIRED_HEADINGS = {
  disease: [
    "Study Snapshot",
    "What It Is",
    "Clinical Pattern",
    "Nursing Lens",
    "Nursing Priorities",
    "Exam Layer",
    "Exam Clues",
    "Related",
  ],
  drug: [
    "Study Snapshot",
    "What It Is",
    "Nursing Lens",
    "Before Giving: What To Check",
    "Monitor For",
    "Major Safety Flags",
    "Exam Layer",
    "Exam Clues",
    "Related",
  ],
};

const RISKY_DIRECTIVE_PATTERNS = [
  /\bdo NOT wait for orders\b/i,
  /\bThe Fix:\b/i,
  /\bTypical Dosage:\b/i,
  /\bCompatibility:\b/i,
  /\bCVL only\b/i,
  /\bMax \d/i,
  /\bSafe in pregnancy\b/i,
  /\bgold standard\b/i,
];

function slugify(value) {
  return value
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function titleFromMarkdown(content, fallback) {
  return content.match(/^#\s+(.+)$/m)?.[1]?.trim() || fallback;
}

function extractLinks(content) {
  return [...content.matchAll(/\[\[([^\]|]+)(?:\|[^\]]+)?\]\]/g)]
    .map((match) => match[1].trim())
    .filter(Boolean);
}

function extractHeadings(content) {
  return new Set(
    [...content.matchAll(/^#{2,3}\s+(.+)$/gm)]
      .map((match) => match[1].trim())
      .filter(Boolean),
  );
}

async function listMarkdownFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true }).catch(() => []);
  return entries
    .filter((entry) => entry.isFile() && entry.name.endsWith(".md") && !entry.name.startsWith("."))
    .map((entry) => path.join(dir, entry.name))
    .sort((a, b) => a.localeCompare(b));
}

async function loadCards(type) {
  const dirName = type === "disease" ? "diseases" : "drugs";
  const files = await listMarkdownFiles(path.join(processedRoot, dirName));
  const cards = [];

  for (const file of files) {
    const content = await readFile(file, "utf8");
    const slug = path.basename(file, ".md");
    const headings = extractHeadings(content);
    const missingHeadings = REQUIRED_HEADINGS[type].filter((heading) => !headings.has(heading));
    const placeholderLinks = extractLinks(content).filter((target) => !slugify(target));
    const notSpecifiedCount = (content.match(/Not specified in source notes\./g) || []).length;
    const riskyDirectiveCount = RISKY_DIRECTIVE_PATTERNS.reduce(
      (count, pattern) => count + (pattern.test(content) ? 1 : 0),
      0,
    );

    cards.push({
      slug,
      title: titleFromMarkdown(content, slug),
      type,
      path: path.relative(repoRoot, file),
      missingHeadings,
      placeholderLinks,
      notSpecifiedCount,
      riskyDirectiveCount,
      linkTargets: extractLinks(content).map(slugify).filter(Boolean),
      wordCount: content.split(/\s+/).filter(Boolean).length,
    });
  }

  return cards;
}

function summarizeCards(cards, allSlugs) {
  return cards.map((card) => {
    const brokenLinks = [...new Set(card.linkTargets.filter((target) => !allSlugs.has(target)))].sort();
    const qualityScore = Math.max(
      0,
      100
        - card.missingHeadings.length * 8
        - brokenLinks.length * 4
        - card.placeholderLinks.length * 10
        - Math.min(card.notSpecifiedCount, 12) * 2
        - card.riskyDirectiveCount * 8,
    );

    return {
      ...card,
      brokenLinks,
      qualityScore,
      status: qualityScore >= 90 ? "student_ready" : qualityScore >= 70 ? "needs_review" : "repair_first",
    };
  });
}

function topIssues(cards) {
  return cards
    .filter((card) => card.status !== "student_ready")
    .sort((a, b) => a.qualityScore - b.qualityScore || a.title.localeCompare(b.title))
    .slice(0, 15)
    .map((card) => ({
      title: card.title,
      type: card.type,
      path: card.path,
      qualityScore: card.qualityScore,
      missingHeadings: card.missingHeadings,
      brokenLinks: card.brokenLinks,
      placeholderLinks: card.placeholderLinks.length,
      notSpecifiedCount: card.notSpecifiedCount,
      riskyDirectiveCount: card.riskyDirectiveCount,
    }));
}

function countByStatus(cards) {
  return cards.reduce((counts, card) => {
    counts[card.status] = (counts[card.status] || 0) + 1;
    return counts;
  }, {});
}

function markdownReport(health) {
  const lines = [
    "# Pulse Content Coverage Report",
    "",
    "Generated by `npm run content:audit`. Use this as the summer database growth dashboard.",
    "",
    "## Summary",
    "",
    `- Diseases: ${health.counts.diseases}`,
    `- Drugs: ${health.counts.drugs}`,
    `- Total cards: ${health.counts.total}`,
    `- Student-ready cards: ${health.statusCounts.student_ready || 0}`,
    `- Needs review: ${health.statusCounts.needs_review || 0}`,
    `- Repair first: ${health.statusCounts.repair_first || 0}`,
    `- Broken wikilink targets: ${health.counts.brokenLinkTargets}`,
    "",
    "## Top Repair Queue",
    "",
  ];

  if (health.topIssues.length === 0) {
    lines.push("No major issues found.");
  } else {
    for (const issue of health.topIssues) {
      const reasons = [
        issue.missingHeadings.length ? `missing headings: ${issue.missingHeadings.join(", ")}` : "",
        issue.brokenLinks.length ? `broken links: ${issue.brokenLinks.join(", ")}` : "",
        issue.placeholderLinks ? `placeholder links: ${issue.placeholderLinks}` : "",
        issue.notSpecifiedCount ? `unspecified sections: ${issue.notSpecifiedCount}` : "",
        issue.riskyDirectiveCount ? `directive wording flags: ${issue.riskyDirectiveCount}` : "",
      ].filter(Boolean);

      lines.push(`- [ ] ${issue.title} (${issue.type}, ${issue.qualityScore}/100): ${reasons.join("; ")}`);
    }
  }

  lines.push("", "## September MVP Content Targets", "");
  lines.push("- [ ] 150 student-ready cards");
  lines.push("- [ ] 0 placeholder wikilinks");
  lines.push("- [ ] Fewer than 25 broken wikilink targets");
  lines.push("- [ ] Top 50 Med-Surg concepts reviewed from the app");
  lines.push("- [ ] Top 50 Pharm concepts reviewed from the app");
  lines.push("");

  return `${lines.join("\n")}\n`;
}

const diseases = await loadCards("disease");
const drugs = await loadCards("drug");
const allSlugs = new Set([...diseases, ...drugs].map((card) => card.slug));
const cards = summarizeCards([...diseases, ...drugs], allSlugs);
const brokenLinkTargets = new Set(cards.flatMap((card) => card.brokenLinks));

const health = {
  generatedAt: new Date().toISOString(),
  counts: {
    diseases: diseases.length,
    drugs: drugs.length,
    total: cards.length,
    brokenLinkTargets: brokenLinkTargets.size,
  },
  statusCounts: countByStatus(cards),
  topIssues: topIssues(cards),
  cards: cards.map((card) => {
    const copy = { ...card };
    delete copy.linkTargets;
    return copy;
  }),
};

await mkdir(path.dirname(reportPath), { recursive: true });
await mkdir(path.dirname(healthPath), { recursive: true });
await writeFile(reportPath, markdownReport(health), "utf8");
await writeFile(healthPath, `${JSON.stringify(health, null, 2)}\n`, "utf8");

console.log(`Audited ${health.counts.total} cards.`);
console.log(`Wrote ${path.relative(repoRoot, reportPath)} and ${path.relative(repoRoot, healthPath)}.`);
